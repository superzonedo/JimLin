// 語言設定存儲鍵
const LANGUAGE_STORAGE_KEY = '@labelx_language_preference';

// 當前語言（之後可以從 userStore 或設定中獲取）
let currentLanguage: Language = 'zh-TW';

// 語言變化監聽器
let languageListeners: ((lang: Language) => void)[] = [];

export function setLanguage(lang: Language) {
  const previousLang = currentLanguage;
  currentLanguage = lang;
  // 無論語言是否相同，都通知所有監聽器，確保組件重新渲染
  languageListeners.forEach(listener => listener(lang));
  // 如果語言確實改變了，強制觸發一次額外的更新
  if (previousLang !== lang) {
    // 已經在上面通知了，這裡不需要重複
  }
}

export function getLanguage(): Language {
  return currentLanguage;
}

// 獲取當前語言（用於臨時檢查）
function getCurrentLanguage(): Language {
  return currentLanguage;
}

// 添加語言變化監聽器
export function addLanguageListener(listener: (lang: Language) => void) {
  languageListeners.push(listener);
  return () => {
    languageListeners = languageListeners.filter(l => l !== listener);
  };
}

// 從 AsyncStorage 載入語言設定
export async function loadLanguage(): Promise<Language> {
  try {
    const savedLang = await AsyncStorage.getItem(LANGUAGE_STORAGE_KEY);
    if (savedLang === 'zh-TW' || savedLang === 'zh-CN' || savedLang === 'en') {
      currentLanguage = savedLang;
      return savedLang;
    }
  } catch (error) {
    console.error('Failed to load language preference:', error);
  }
  return currentLanguage;
}

// 保存語言設定到 AsyncStorage
export async function saveLanguage(lang: Language): Promise<void> {
  try {
    await AsyncStorage.setItem(LANGUAGE_STORAGE_KEY, lang);
    // 無論是否相同，都更新並觸發監聽器
    if (currentLanguage !== lang) {
      setLanguage(lang);
    } else {
      // 即使相同，也觸發監聽器，確保組件重新渲染
      setLanguage(lang);
    }
  } catch (error) {
    console.error('Failed to save language preference:', error);
  }
}

// 獲取翻譯文字
export function t(key: string, params?: Record<string, string | number>): string {
  const keys = key.split('.');
  let value: any = translations[currentLanguage];
  
  for (const k of keys) {
    value = value?.[k];
    if (value === undefined) {
      // 如果找不到，回退到繁體中文
      value = translations['zh-TW'];
      for (const k2 of keys) {
        value = value?.[k2];
      }
      break;
    }
  }
  
  if (typeof value !== 'string') {
    // 如果還是找不到，嘗試返回 key 的最後一部分
    const lastKey = keys[keys.length - 1];
    return lastKey || key;
  }
  
  // 替換參數
  if (params) {
    return value.replace(/\{(\w+)\}/g, (match, paramKey) => {
      return params[paramKey]?.toString() || match;
    });
  }
  
  return value;
}

// 導出翻譯對象，方便直接訪問
export function getTranslations(lang: Language = currentLanguage) {
  return translations[lang];
}
